    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_scroll);
        MyListView mListView = (MyListView) findViewById(R.id.id_recyclerview);
        MyScrollView mYscrollview = (MyScrollView) findViewById(R.id.my_scrollview);
        mListView.setScrollview(mYscrollview);
        mYscrollview.setListView(mListView);
    }
