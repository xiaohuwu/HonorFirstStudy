public abstract class BasePerson {
    /**
     * 名字
     */
    private String name;
    /**
     * 年龄
     */
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    /**
     * 睡觉的方法
     */
    public void sleep() {
        System.out.println("睡觉");
    }

    /**
     * 吃饭的方法，每个人吃的不一样,所有用抽象方法
     */
    public abstract void haveEat();
}
