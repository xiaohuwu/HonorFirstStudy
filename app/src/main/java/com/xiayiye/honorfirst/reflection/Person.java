public class Person {
    private String name;
    public int age;
    private String sex;
    public String work;

    public Person() {
    }

    private Person(String name) {
        this.name = name;
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    private Person(String name, int age, String sex) {
        this.name = name;
        this.age = age;
        this.sex = sex;
    }

    public Person(String name, int age, String sex, String work) {
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.work = work;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getWork() {
        System.out.println("打印：" + work);
        return work;
    }

    public void setWork(String work) {
        this.work = work;
    }

    private void getSelfName(String name) {
        System.out.println("打印修改后的名字：" + name);
    }

    private String getArgs(String name) {
        return "带返回值：" + name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                ", sex='" + sex + '\'' +
                ", work='" + work + '\'' +
                '}';
    }
}
